const WEEK1_START = new Date('2026-03-02');

function weekToDeadline(lastWeek: number): string {
  const d = new Date(WEEK1_START);
  d.setDate(d.getDate() + (lastWeek - 1) * 7 + 4); // пятница конца недели
  return d.toISOString().split('T')[0];
}

async function login() {
  const res = await fetch(`${API_URL}/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ email: 'admin@alashed.kz', password: 'admin123' })
  });
  const data = await res.json();
  if (!res.ok) throw new Error(`Login failed: ${JSON.stringify(data)}`);
  return data.accessToken || data.token || data.data?.accessToken;
}

async function createTask(token: string, task: any) {
  const res = await fetch(`${API_URL}/tasks`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${token}` },
    body: JSON.stringify(task)
  });
  if (!res.ok) {
    const err = await res.text();
    throw new Error(`Failed: ${err}`);
  }
  return await res.json();
}


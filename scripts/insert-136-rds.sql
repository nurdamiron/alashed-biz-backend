  SELECT COUNT(*) INTO assignee_count FROM task_assignees;
  RAISE NOTICE '=== ГОТОВО ===';
  RAISE NOTICE 'Задач создано: %', task_count;
  RAISE NOTICE 'Назначений создано: % (% задач x 3 исполнителя)', assignee_count, task_count;

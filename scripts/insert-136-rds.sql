-- Временная функция для расчёта дедлайнов
CREATE OR REPLACE FUNCTION week_deadline(last_week INT) RETURNS DATE AS $$
BEGIN
  RETURN DATE '2026-03-02' + ((last_week - 1) * 7 + 4) * INTERVAL '1 day';
END;
$$ LANGUAGE plpgsql;

